void solve()
{
    int n, m;
    cin >> n >> m;

    string s1, s2;
    cin >> s1 >> s2;

    int j = 0;

    int cnt = 0;

    for (int i = 0; i < m; i++)
    {
        if (s1[j] == s2[i])
        {
            cnt++;
            j++;
        }
    }

    cout << cnt << endl;
}
